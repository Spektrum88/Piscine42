#include <stdio.h>

int main()
{
	int u;
	int d;

	u = 2;
	while ( u < 5)
	{
		
		d = 1;
		while (d <= 2)
		{
			printf("%d\n %d\n", u, d);
			d++;
		}
	
	u++;
	}
}
